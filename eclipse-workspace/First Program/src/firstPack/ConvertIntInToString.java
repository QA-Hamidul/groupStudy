package firstPack;

public class ConvertIntInToString {
	
	public static void main(String args[]) { 
	    int a = 786; 
	    int b = -986; 
	    String str1 = Integer.toString(a); 
	    String str2 = Integer.toString(b); 
	    System.out.println("String str1 = " + str1);  
	    System.out.println("String str2 = " + str2); 
	  } 

}
